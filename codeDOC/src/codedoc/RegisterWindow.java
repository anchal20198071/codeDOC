    //Logic to save or insert the user information into the user_registration Table
    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        String name = jTextFieldName.getText();
        String email = jTextFieldEmailId.getText();
        String pass = jPasswordFieldPass.getText();
        String cpass = jPasswordFieldCPass.getText();
        String mob = jTextFieldMobileNum.getText();
        String gender = "Male";
        if (jRadioButtonFemale.isShowing()) {
            gender = "Female";
        }
        Date dob = jDateChooserDOB.getDate();
        
        int length= mob.length();

        //apply the validation logic for checking all the controls are empty or not
        if (name.trim().equals("") || name.trim().equals("Enter Full Name")
                || email.trim().equals("") || email.trim().equals("Enter Email-Id")
                || pass.trim().equals("") || pass.trim().equals("Enter Password")
                || cpass.trim().equals("") || cpass.trim().equals("Conform Password")
                || mob.trim().equals("") || mob.trim().equals("Enter Mobile Number")) {
            JOptionPane.showMessageDialog(null, "All Fields are Compulsory to Fill. "
                    + "Kindly Check the missing Fields in the Registration Form.....!!!!!");
        }else if(!(Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", jTextFieldEmailId.getText()))){
            JOptionPane.showMessageDialog(null, "Please enter a valid email", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(length<10){
            JOptionPane.showMessageDialog(null, "Please enter a valid 10 digit Mobile Number", "Error", JOptionPane.ERROR_MESSAGE);
    }
        else if (pass.equals(cpass)) {
            if (dob == null) {//if date of Birth is not choosen
                JOptionPane.showMessageDialog(null, "Please select / Enter Date of Birth");
                jDateChooserDOB.grabFocus();
            } else {//if everything is fine
                //all swing control fields has been selected
                //then we can save / insert/ register into database table
                JOptionPane.showMessageDialog(null, " All fields are filled");
                
                try {
                    //converting date to string
                    SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
                    String dob1 = sdf.format(jDateChooserDOB.getDate());

                    Socket soc = new Socket("localhost", 9886);
                    String[] s = {name, email, pass, mob, gender, dob1};

                    PrintWriter out = new PrintWriter(soc.getOutputStream(), true);
                    out.println("15");

                    System.out.println("Sending Data to the server side");

                    for (int i = 0; i < 6; i++) {
                        out.println(s[i]);
                    }

                    BufferedReader in = new BufferedReader(new InputStreamReader(soc.getInputStream()));
                    int serverResponse = Integer.parseInt(in.readLine());
                    System.out.println(serverResponse);

                    if (serverResponse == 1) {
                        //JOptionPane.showMessageDialog(null, "Userrrr Details Saved successfully");
                        resetActionPerformed(evt);
                    }

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, name + " User not inserted / saved "
                            + "Exception e : " + e);
                }

            }//DOB else
        }// pass and cpass
        else {
            //if password and conform password are not same
            JOptionPane.showMessageDialog(null, " Password and Conform Password must be same....!!");
            jPasswordFieldPass.setText("");
            jPasswordFieldCPass.setText("");
        }

    }//GEN-LAST:event_registerActionPerformed

