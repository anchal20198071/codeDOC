        //to enter valid phone number
    private void jTextFieldMobileNumKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldMobileNumKeyPressed
        String phoneNum=  jTextFieldMobileNum.getText();
        
        //get length of the string
        int length= phoneNum.length();
        
        char c= evt.getKeyChar();
        
        //check for number 0 to 9
        if(c>='0' && c<='9'){
            //check for length not more than 10 digit
            if(length<10){
                //editable true
                jTextFieldMobileNum.setEditable(true);
            }else{
                //not editable if length is equal to or more than 10
                jTextFieldMobileNum.setEditable(false);
            }
        }else{
            //allowing for keys backspace and delete for edit
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode()==KeyEvent.VK_DELETE){
                //then allow editable
                jTextFieldMobileNum.setEditable(true);
            }else{
                 jTextFieldMobileNum.setEditable(false);
            }
        }
    }//GEN-LAST:event_jTextFieldMobileNumKeyPressed

